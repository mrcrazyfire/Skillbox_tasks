import math


"""
Задача 1. Среднее арифметическое
Программа получает от пользователя два числа — a и b. Реализуйте функцию,
которая принимает на вход числа a и b, считает и выводит в консоль среднее арифметическое всех чисел из отрезка [a; b].
 Обеспечьте контроль ввода: не забывайте, что а всегда должно быть меньше, чем b.

Пример:
Введите левую границу: 3
Введите правую границу: 8

Среднее: 5.5

Усложнение: сделайте это без использования циклов.
"""

def average_interval(a, b):
    if a >= b:
        print("Ошибка: левая граница должна быть меньше правой.")
    count = b - a + 1
    total = (a + b) * count / 2  # сумма арифметической прогрессии
    avg = total / count
    print("Среднее:", avg)


