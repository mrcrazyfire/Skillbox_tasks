def func():
    start = int(input("–í–≤–µ–¥–∏—Ç–µ –Ω–∞—á–∞–ª–æ –æ—Ç—Ä–µ–∑–∫–∞: "))
    finish = int(input("–í–≤–µ–¥–∏—Ç–µ –∫–æ–Ω–µ—Ü –æ—Ç—Ä–µ–∑–∫–∞: "))
    step = int(input("–í–≤–µ–¥–∏—Ç–µ —à–∞–≥: "))

    if step > 0:
        start, finish = finish, start
        step = -step

    for x in range(finish, start - 1, step):
        y = x ** 3 + 2 * x ** 2 - 4 * x + 1
        print(f"–í —Ç–æ—á–∫–µ {x} —Ñ—É–Ω–∫—Ü–∏—è —Ä–∞–≤–Ω–∞ {y}")


