def schoolarship():
    educational_grant = int(input("–í–≤–µ–¥–∏—Ç–µ –µ–∂–µ–º–µ—Å—è—á–Ω—É—é —Å—Ç–∏–ø–µ–Ω–¥–∏—é: "))
